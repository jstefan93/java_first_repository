public class DataTypes {
    public static void main(String[] args) {
        System.out.println(10); //liczba całkowita
        System.out.println(10.5); //liczba zmiennoprzecinkowa
        System.out.println("text w javie"); //tekst
        System.out.println('a'); // znak w pojedynczym cudzysłowie
        System.out.println(true); // wartość logiczna boolean
        System.out.println(false); // wartość logiczna boolean
    }
}
