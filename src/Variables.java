public class Variables {
    public static void main(String[] args) {

        // deklaracja zmiennej dla liczby całkowitej
        int number10;
        number10 = 10;

        // można zmienną zadeklarować i przypisać wartość w jednej linii
        int number12 = 12;
        System.out.println(number10);
        System.out.println(number12);

        // deklaracja zmiennej dla liczby zmiennoprzecinkowej
        double doubleNumber = 10.5;
        System.out.println(doubleNumber);

        // text
        String someText = "tekst w javie";
        System.out.println(someText);

        // znak
        char someChar = 's';
        System.out.println(someChar);

        // boolean
        boolean trueValue = true;
        System.out.println(trueValue);
    }
}
